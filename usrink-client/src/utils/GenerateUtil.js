/**
 * 随机获取一些文本
 */
const getRandomTexts = () => {
    const selectedTexts = [];
    const numTexts = Math.floor(Math.random() * 2) + 3; // 随机选择 3 到 4 条文本

    // 从原始文本数组中随机选择文本，并将其添加到选定的文本数组中
    for (let i = 0; i < numTexts; i++) {
        const randomIndex = Math.floor(Math.random() * textArray.length);
        if (selectedTexts.includes(textArray[randomIndex])) {
            continue;
        }
        selectedTexts.push(textArray[randomIndex]);
    }

    return selectedTexts;
}

